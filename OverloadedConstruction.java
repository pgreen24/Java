public class OverloadedConstruction {
    public static void main(String[] args) {
        pizza jk = new pizza();
        System.out.println("here are the ingredient of your pizza: ");
        System.out.println(jk.bread);
        System.out.println(jk.sauce);
        System.out.println(jk.chess);
        System.out.println(jk.topping);
    }
}